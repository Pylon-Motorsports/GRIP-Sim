// clang-format on

const uint8_t* HudOverlay::glyphData(char ch)
{
    int idx = static_cast<int>(ch) - 32;
    if (idx < 0 || idx > 94) idx = 0; // fallback to space
    return g_font5x7[idx];
}

// ============================================================================
// Geometry helpers
// ============================================================================

void HudOverlay::drawRect(float x, float y, float w, float h,
                          float r, float g, float b, float a)
{
    uint32_t base = static_cast<uint32_t>(verts_.size());
    glm::vec3 n { 0.f, 0.f, 1.f };
    glm::vec4 col { r, g, b, a };

    verts_.push_back({ {x,     y,     0.f}, n, col });
    verts_.push_back({ {x + w, y,     0.f}, n, col });
    verts_.push_back({ {x + w, y + h, 0.f}, n, col });
    verts_.push_back({ {x,     y + h, 0.f}, n, col });

    idxs_.push_back(base);     idxs_.push_back(base + 1); idxs_.push_back(base + 2);
    idxs_.push_back(base);     idxs_.push_back(base + 2); idxs_.push_back(base + 3);
}

void HudOverlay::drawChar(char ch, float x, float y, float pixSize,
                          float r, float g, float b, float a)
{
    const uint8_t* glyph = glyphData(ch);
    for (int row = 0; row < 7; ++row) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 5; ++col) {
            if (bits & (0x10 >> col)) {
                float px = x + col * pixSize;
                float py = y + row * pixSize;
                drawRect(px, py, pixSize * 0.9f, pixSize * 0.9f, r, g, b, a);
            }
        }
    }
}

float HudOverlay::drawString(const std::string& text, float x, float y, float pixSize,
                             float r, float g, float b, float a)
{
    float cx = x;
    for (char ch : text) {
        drawChar(ch, cx, y, pixSize, r, g, b, a);
        cx += 6.f * pixSize; // 5 pixels + 1 gap
    }
    return cx - x;
}

float HudOverlay::drawLabelValue(const std::string& label, const std::string& value,
                                 float x, float y, float pixSize,
                                 float lr, float lg, float lb,
                                 float vr, float vg, float vb)
{
    float w = drawString(label, x, y, pixSize, lr, lg, lb);
    drawString(value, x + w, y, pixSize, vr, vg, vb);
    return 8.f * pixSize; // line height
}

// ============================================================================
// Main HUD build
// ============================================================================

void HudOverlay::buildFrame(const VehicleState& state, float windowW, float windowH)
{
    verts_.clear();
    idxs_.clear();

    // Scale factor: design for 720p, scale proportionally
    float scale = windowH / 720.f;
    float pix   = 3.f * scale;   // pixel size for normal text
    float bigPix = 5.f * scale;  // pixel size for speed display

    // Colors
    const float W = 1.f, G = 0.7f; // white and gray
    // Threshold colors
    auto warnColor = [](float val, float warn, float crit,
                        float& r, float& g, float& b) {
        if (std::abs(val) > crit)       { r = 1.f; g = 0.2f; b = 0.2f; } // red
        else if (std::abs(val) > warn)  { r = 1.f; g = 0.8f; b = 0.2f; } // yellow
        else                            { r = 0.2f; g = 1.f; b = 0.4f; } // green
    };

    // === Top-right: Speed ===
    float speedKph = state.speedMs * 3.6f;
    char buf[64];
    std::snprintf(buf, sizeof(buf), "%3.0f", std::abs(speedKph));
    float speedX = windowW - 22.f * bigPix;
    float speedY = 2.f * bigPix;
    drawString(buf, speedX, speedY, bigPix, W, W, W);
    drawString("km/h", speedX + 24.f * bigPix * 0.6f, speedY + 4.f * bigPix, pix, G, G, G);

    // === Top-right below speed: RPM bar + gear ===
    float rpmX = windowW - 28.f * pix * 6.f * 0.17f - 10.f * pix;
    float rpmY = speedY + 10.f * bigPix;

    // RPM bar background
    float barW = 200.f * scale;
    float barH = 12.f * scale;
    drawRect(rpmX, rpmY, barW, barH, 0.15f, 0.15f, 0.15f, 0.8f);

    // RPM bar fill
    float rpmFrac = std::clamp(state.engineRpm / 7400.f, 0.f, 1.f);
    float rpmR = (rpmFrac > 0.85f) ? 1.f : 0.2f;
    float rpmG = (rpmFrac > 0.85f) ? 0.2f : 0.8f;
    float rpmB = 0.2f;
    drawRect(rpmX + 1, rpmY + 1, (barW - 2) * rpmFrac, barH - 2, rpmR, rpmG, rpmB, 0.9f);

    // RPM text
    std::snprintf(buf, sizeof(buf), "%4.0f RPM", state.engineRpm);
    drawString(buf, rpmX, rpmY + barH + 2.f * pix, pix, G, G, G);

    // Gear indicator
    std::snprintf(buf, sizeof(buf), "%d", state.currentGear);
    float gearX = rpmX + barW + 8.f * pix;
    drawString(buf, gearX, rpmY - 2.f * pix, bigPix, W, W, 0.5f);

    // === Bottom-left: Debug telemetry ===
    float telX = 10.f * scale;
    float telY = windowH - 20.f * pix;
    float lineH = 9.f * pix;

    // Slip angle
    float slipDeg = state.slipAngleRad * 180.f / 3.14159f;
    float sr, sg, sb;
    warnColor(slipDeg, 5.f, 15.f, sr, sg, sb);
    std::snprintf(buf, sizeof(buf), "%+6.1f deg", slipDeg);
    drawLabelValue("SLIP  ", buf, telX, telY, pix, G, G, G, sr, sg, sb);
    telY -= lineH;

    // Body roll
    float rollDeg = state.rollRad * 180.f / 3.14159f;
    float rr, rg, rb;
    warnColor(rollDeg, 3.f, 8.f, rr, rg, rb);
    std::snprintf(buf, sizeof(buf), "%+5.1f deg", rollDeg);
    drawLabelValue("ROLL  ", buf, telX, telY, pix, G, G, G, rr, rg, rb);
    telY -= lineH;

    // Body pitch
    float pitchDeg = state.pitchRad * 180.f / 3.14159f;
    float pr, pg, pb;
    warnColor(pitchDeg, 3.f, 8.f, pr, pg, pb);
    std::snprintf(buf, sizeof(buf), "%+5.1f deg", pitchDeg);
    drawLabelValue("PITCH ", buf, telX, telY, pix, G, G, G, pr, pg, pb);
    telY -= lineH;

    // Weight front
    float wf = state.weightFront;
    float wr2, wg2, wb2;
    warnColor(wf - 0.5f, 0.15f, 0.25f, wr2, wg2, wb2);
    std::snprintf(buf, sizeof(buf), "F:%2.0f R:%2.0f", wf * 100.f, (1.f - wf) * 100.f);
    drawLabelValue("WGHT  ", buf, telX, telY, pix, G, G, G, wr2, wg2, wb2);
    telY -= lineH;

    // Suspension lengths (4 mini bars)
    drawString("SUSP", telX, telY, pix, G, G, G);
    float suspBarX = telX + 7.f * 6.f * pix;
    const char* cornerNames[] = { "FL", "FR", "RL", "RR" };
    for (int c = 0; c < 4; ++c) {
        float bx = suspBarX + c * 50.f * scale;
        float suspFrac = std::clamp(state.suspLength[c] / 0.35f, 0.f, 1.f);
        // Bar background
        drawRect(bx, telY, 35.f * scale, 7.f * pix, 0.15f, 0.15f, 0.15f, 0.8f);
        // Bar fill — color by compression
        float cr, cg, cb;
        if (suspFrac < 0.3f)      { cr = 1.f; cg = 0.2f; cb = 0.2f; }
        else if (suspFrac < 0.5f) { cr = 1.f; cg = 0.8f; cb = 0.2f; }
        else                      { cr = 0.2f; cg = 0.8f; cb = 0.4f; }
        drawRect(bx + 1, telY + 1, (35.f * scale - 2) * suspFrac, 7.f * pix - 2,
                 cr, cg, cb, 0.9f);
        // Label
        drawString(cornerNames[c], bx + 5.f * scale, telY - lineH * 0.8f, pix * 0.7f, G, G, G);
    }
    telY -= lineH;

    // Segment info
    std::snprintf(buf, sizeof(buf), "Seg %d  %.0f%%", state.segmentIndex, state.segmentProgress * 100.f);
    drawString(buf, telX, telY, pix * 0.8f, 0.5f, 0.5f, 0.5f);
}
